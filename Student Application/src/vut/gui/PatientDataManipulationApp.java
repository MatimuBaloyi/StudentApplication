    private void cmbSelectActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_cmbSelectActionPerformed
    {//GEN-HEADEREND:event_cmbSelectActionPerformed

        NumberFormat currformat = NumberFormat.getCurrencyInstance();
        String idNr = cmbSelect.getSelectedItem().toString();

        try
        {

            txtSurname.setText(objPatient.getPatientObj(idNr).getSurname());
            txtPatientType.setText(objPatient.getPatientObj(idNr).getPatientType().toString());
            txtAmount_Due.setText(( currformat.format(objPatient.getPatientObj(idNr).getAmountDue())));

        }
        catch (NotFoundException ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_cmbSelectActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnUpdateActionPerformed
    {//GEN-HEADEREND:event_btnUpdateActionPerformed
        Double amountDue = Double.parseDouble(JOptionPane.showInputDialog("Enter Amount Paid: "));
        String idNr = cmbSelect.getSelectedItem().toString();
        objPatient = new PatientPD();

        try
        {
            objPatient.updateAmountDue(amountDue, idNr);
            fillCombobox();
        }
        catch (NotFoundException ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }


    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDisplayTotalActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnDisplayTotalActionPerformed
    {//GEN-HEADEREND:event_btnDisplayTotalActionPerformed
        NumberFormat formatter = NumberFormat.getCurrencyInstance();
        try
        {

            lblTotal_Patients.setText(formatter.format(objPatient.calculateTotalAmountDue()));
            lblTotal_OutPatients.setText(formatter.format(objPatient.calculateTotalOutPatients()));
            lblTotal_InPatients.setText(formatter.format(objPatient.calculateTotalInPatients()));

        }
        catch (NotFoundException ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnDisplayTotalActionPerformed

