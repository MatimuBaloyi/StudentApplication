    private void fillCombobox()
    {
        //declare a local arraylist
        ArrayList<String> arListIDNumbers = new ArrayList<>();

        //clear the combo box
        cmbSelect.removeAll();
        try
        {
            //transferring all IDNumbers to the local arraylist
            arListIDNumbers = objPatient.getIDnumbers();

            //looping through the arraylist storing ID Numbers in the combo box
            for (int cnt = 0; cnt < arListIDNumbers.size(); cnt++)
            {
                cmbSelect.addItem(arListIDNumbers.get(cnt));
            }
        }
        catch (NotFoundException ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

